package recapDemoClasses;

public class DortIslem {
	public int toplama(int sayi1, int sayi2) {
		return sayi1 + sayi2;
	}
	public int cıkarma(int sayi1, int sayi2) {
		return sayi1 - sayi2;
	}
	public int carpma(int sayi1, int sayi2) {
		return sayi1 * sayi2;
	}
	public float bolme(float sayi1, float sayi2) {  // sayi1 ve sayi2 flaot olmalı yoksa nanay :)
		return sayi1/sayi2;
	}

}
